# Escape Sequence in python

# \n - Newline
print('Hello\nStudents')

# \\ - Backslash
print('C:\\Python36\\Lib')

# \' - Single Quote
print("This is my \'life\'")

# \" - Double Quote
print('This is my \"Life\"')

# \a - ASCII Bell
print('\aHello')

# \b - ASCII Backspace
print("Hello  \bWorld!")

# \f - ASCII formfeed
print("Hello \fWorld")

# \r - ASCII carriage return
print('Hello \rMister')

# \t - ASCII Horizontal tab
print('Hello \tWorld')

# \v - ASCII vertical tab
print('Hello \vWorld')


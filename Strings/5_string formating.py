# String Formating
'''The string formating operator is python is %'''
# Example
# %c - Representing a character
print("This is the first Alphabet %c" %'A')

# %s - Represents a string
print("My name is %s" %('Python'))

# %d - Represents a signed decimal integer
print('My height is %d' %(96))

# %u - Represents an unsigned decimal integer
print('My weight is %u' %(10))

# %o - Represents an octal integer
print('This is an octal integer %o' %(77))

# %x - Represents a hexadecimal integer in lowercase
print('This is a hexadecimal integer %x' %(255))

# %e - Represents a real number in exponential notation with lowercase e
print('This is an exponential notation %e' %(12345.6789))

# %f - Represents a floating point real number
print('This is a floating point real number %f' %(2243.524345))

# %g - Represents a floating point real number in shorter form
print('This is a floating point real number %g' %(2243.5243))
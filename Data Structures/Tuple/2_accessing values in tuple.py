# Accessing values in tuple
'''The elements of a tuple are accessed by referring to the index number with in square brackets.
    There are two types of indexing 1. positive indexing, 2. negative indexing'''
# Positive indexing
# Example
x = ('red', 'blue', 'green')
print('x=', x)
print('x[0]=', x[0])
print('x[1]=', x[1])
print('x[2]=', x[2])
# print('x[3]=', x[3])  # IndexError: tuple index out of range

# Negative indexing
print('x[-1]=', x[-1])
print('x[-2]=', x[-2])
print('x[-3]=', x[-3])
# print('x[-4]=', x[-4]) # Index error


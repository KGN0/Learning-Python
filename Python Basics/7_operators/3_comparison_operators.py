# Comparison Operator
'''
Comparison (relational) operators, compares the values of two operands and returns True or False based on the condition met.
There are six types of comparison operators viz.
'''
# Equal to operator (==)
a = 5; b = 10
print(f'{a} == {b} is {a == b}')

# Not equal to operator (!=)
print(f'{a} != {b} is {a != b}')

# Greater than operator (>)
print(f'{a} > {b} is {a > b}')

# Less than operator (<)
print(f'{a} < {b} is {a < b}')

# Greater than or equal to (>=)
print(f'{a} >= {b} is {a >= b}')

# Less than or equal to (<=)
print(f'{a} <= {b} is {a <= b}')

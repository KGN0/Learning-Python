# Boolean operators
'''
There are three boolean operators that act on bool type literals and provide bool type output'''
a  = True
b = False

print(f"True and True is {True and True}")
print(f"{a} and {b} is {a and b}")
print(f"{a} or {b} is {a or b}")
print(f"not {a} is {not a}")
print(f"not {b} is {not b}")
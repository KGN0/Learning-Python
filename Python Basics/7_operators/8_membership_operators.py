# Membership operatos
#There are two types of operators

# in operator 
# it return true if the element is found in the specified sequence otherwise false

# not in operator
# it return false if the element is found in the specified sequence otherwise true

a = ['Python', "java"]

print(f"Python in a: {'Python' in a}")

print(f"c not in a {'c' not in a}")


# Assignment operators
'''
Assignment operators assign values on the right hand side of the operator to  the variable on the left hand side
The right hand side of the assignment operator can contain a constant/variable/expression.
'''
# Simple assignment
a = 10
print(f"The value of a is {a}")

# Addition assignment
a += 8
print(f"The value of a after addtion assignment with 8 is {a}")

# Subtraction assignment
a -= 6
print(f"The value of a after subtraction assignment with 6 is {a}")

# Multiplication assignment
a *= 5
print(f"The value of a after multiplication assignment with 5 is {a}")

# Float division assignment
a /= 6
print(f"The value of a after float division assignment with 6 is {a}")

# Integer division assignment
a //= 5
print(f"The value of a after integer division assignment with 5 is {a}")

# Exponentiation assignment
a **= 8
print(f"The value of a after exponentiation assignment with 8 is {a}")

# Remainder assignment
a %= 3
print(f"The value of a after remainder assignment with 3 is {a}")

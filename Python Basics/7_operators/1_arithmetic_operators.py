# Operators
'''
An operator is a symbol that perfom specifi operations on variables and values.
The value that the operator operates on is called the operand
python is rich in built in operators'''

# Arithmetic operators
'''
Arithmetic operators perform mathematical operations like 

+ is Addition
- is Subtraction
* is Multiplication
/ is Float Division
// is Integer Division
** is Exponentiation
% is Remainder
'''

# Addition
a = 1.50
b = 1.64
c= a + b
print('The addition of',a,'and',b, '=',c)

# Subtraction
a = 5
b = 1.86
c = a - b
print(f"The subtraction of {a} and {b} is {c}")

# Multiplication
a = 1.57
b = 2
c = a * b
print(f"The multiplication of {a} and {b} is {c}")

# Float division
a = 3.14
b = 1
c = a / b
print(f"The division of {a} and {b} is {c}")

# Integer division
a = 3.14
b = 1
c = a // b
print(f"The integer division of {a} and {b} is {c}")

# Exponentiation
a = 3.14
b = 1
c = a ** b
print(f"The exponentiation of {a} and {b} is {c}")

# Remainder
a = 10
b = 7
c = a % b
print(f"The remainder of {a} divided by {b} is {c}")